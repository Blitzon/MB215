a = float(input("Enter the first float number (a): "))
b = float(input("Enter the second float number (b): "))


addition = a + b
subtraction = a - b
multiplication = a * b
division = a / b
integer_division = a // b
remainder = a % b
exponent = a ** b

print(f"1. Addition: {a} + {b} = {addition}")
print(f"2. Subtraction: {a} - {b} = {subtraction}")
print(f"3. Multiplication: {a} * {b} = {multiplication}")
print(f"4. Division: {a} / {b} = {division}")
print(f"5. Integer Division: {a} // {b} = {integer_division}")
print(f"6. Remainder: {a} % {b} = {remainder}")
print(f"7. Exponent: {a} ** {b} = {exponent}")
